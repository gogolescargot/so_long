#include "../../inc/ft_printf.h"

int	ft_putstr(char *s)
{
	if (!s)
	{
		if (write(1, "(null)", 6) == -1)
			return (-1);
		return (0);
	}
	if (write(1, s, ft_strlen(s)) == -1)
		return (-1);
	return (0);
}
