#include "../inc/so_long.h"

void	replace_x(char **map)
{
	ssize_t	i;
	ssize_t	j;

	i = -1;
	j = -1;
	while (map[++i])
	{
		while (map[i][++j])
		{
			if (map[i][j] == 'X')
				map[i][j] = 'x';
		}
		j = -1;
	}
}
